/*
    Very simple, given an integer or a floating-point number, find its opposite.

    Examples:

    1: -1
    14: -14
    -34: 34
*/

function opposite(number) {
    return number * -1;
}

//TEST
console.log(opposite(1)); //-1
console.log(opposite(14)); //-14
console.log(opposite(-34)); //34

